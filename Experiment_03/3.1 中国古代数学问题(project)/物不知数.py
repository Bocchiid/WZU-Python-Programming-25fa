"""一些物品，不知道有多少个，3个3个数的话，还多出2个；5个5个数则多出3个；
7个7个数也会多出2个。输入一个正整数，从小到大依次输出所有不超过输入数字
且满足条件的物品数量，有多个答案时每行输出一个。
例如输入：200
输出：
23
128
"""
n = int(input())
##############Begin#####################
flag = False

for m in range(n + 1):
    if (m % 3 == 2 and m % 5 == 3 and m % 7 == 2):
        print(m)
        flag = True

if (not flag):
    print('No solution!')
##############End#####################
